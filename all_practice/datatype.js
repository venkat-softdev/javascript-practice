// Datatypes in javascript

let a = 10;
let b = 20;
let c = a + b;

console.log(typeof c, c);

// string type
let name = "venkat";
console.log(typeof name, name);

// Boolean type

let person = "venkat";
let isstudent = true;
console.log(typeof person, person);
console.log(typeof isstudent, isstudent);

// Null type

let items = ["venkat", 19, "vadalur"];

items = null;
console.log(items);

// undefined

console.log(list);

var list = ["apple", "orange", "mango"];

// array

let array = ["venkat", "arish", "paratha", 22];

console.log(array);
console.log(array[0]);

// object type

let person2 = {
  name: "venkat",
  age: 19,
  city: "vadalur",
  isstudent: true,
};
console.log(person2);
console.log(person2.name);
console.log(person2.age);

